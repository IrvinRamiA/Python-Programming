# Numbers - Integers
my_int = 7
print(my_int)

# Numbers - Floating point
my_float = 7.0
print(my_float)
my_float = float(8)  # Casting from 'int' to 'float'
print(my_float)

# Strings
my_string = 'hello 1'
print(my_string)
my_string = "hello 2"
print(my_string)
my_string = "Don't worry about apostrophes"
print(my_string)

# Simple operators can be executed on numbers and strings
one = 1
two = 2
three = one + two
print(three)

hello = "hello"
world = "world"
hello_world = hello + " " + world
print(hello_world)

# Assignments can be done on more than one variable simultaneously
a, b = 3, 4
print(a, b)

