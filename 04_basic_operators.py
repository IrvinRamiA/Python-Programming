# Arithmetic operators
number = 1 + 2 * 3 / 4.0
print(number)

remainder = 11 % 3
print(remainder)

squared = 7 ** 2
cubed = 2 ** 3
print("7^2 = %d" % squared)
print("2^3 = %d" % cubed)

# Operators with strings
hello_world = "hello" + " " + "world"
print(hello_world)

lots_of_hellos = "hello " * 10
print(lots_of_hellos)

# Operators with lists
even_numbers = [2, 4, 6, 8]
odd_numbers = [1, 3, 5, 7]
all_numbers = odd_numbers + even_numbers
print(all_numbers)
print([1, 2, 3] * 3)
